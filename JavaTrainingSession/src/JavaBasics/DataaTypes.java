package JavaBasics;

public class DataaTypes {

	public static void main(String[] args) {
		
		//  Important interview Question : Primitive datatypes are int, double, char,boolean
		//1)Int
		int i= 20;
		i = 30;
		int j= 90;
		
		// Short cut for print function is type syso then cntl+space
		
		System.out.println(j);
		System.out.println(i+j);
		
		//2) double
		double d =12.33;
		double a = 34.34;
		
		System.out.println(a+d);
		
		//3) char: only single digit value, should be written within single quotes
		char c ='a';
		char e = '3';
		System.out.println(e);
		
		//4) Boolean:
		boolean b1 = true;
		boolean b2 = false;
		System.out.println(b1);
		
		
		//5) String is a class not a data type.
		String s = "Hello World";
		String s3 = " Hi there";
		String s4 = " 1000";
		String s2 = "12.33";
		System.out.println(s2);
		
		
		
		
		
		
		

		
		
		
		
		
		
	}

}
